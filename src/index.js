const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};
function decode(expr) {
  let words = expr.match(/.{1,10}/g);

  words = words.map(el => 
    el
      .replace(/^(00)+/g, '')
      .replace(/11/g, '-')
      .replace(/10/g, '.')
      .replace(/\*\*\*\*\*\*\*\*\*\*/g, ' ')
  );

  for(let i = 0; i < words.length; i++) {
    if(words[i] != ' ') {
      words[i] = MORSE_TABLE[words[i]];
    }
  }

  return words.join('');
}

module.exports = {
    decode
}